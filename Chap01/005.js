/* _____________ 계산 프로그램 만들기 _____________ */

/*
  ***** 적용 시 *****
  사칙 연산을 사용하고 싶을 때

  ***** Syntax *****
  - + : 덧셈
  - - : 뺄셈
  - * : 곱셈
  - / : 나눗셈
  - % : 나머지
  - ** : 제곱

  6개의 기본연산자가 있다. 숫자 데이터 사이에 연산자를 입력하면,
  해당 계산이 실행된다. 
*/

console.log(100 + 200); // 300
console.log(200 - 80); // 120
console.log(100 * 3); // 300
console.log(400 / 5); // 80
console.log(402 % 5); // 2
console.log(2 ** 3); // 8
