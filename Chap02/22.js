/* _____________ 어림 계산하기 (반올림, 올림, 버ㅁ) _____________ */

/*
  ***** 적용 *****
  - 소수점을 버리는 계산을 할  때
  - 화면 크기를 정수로 조절할 때


  ***** Syntax *****
    구문 : 의미 : 변환
    Math.round(값) : 반올림하기 : 숫자
    Math.floor(값) : 버림하기(값보다 작고 제일 가까운 정수 변환) : 숫자
    Math.ceil(값) : 올림하기(값보다 크고 제일 가까운 정수 변환) : 숫자
    Math.trunc(값) : 값의 정수 부분만을 반환 : 숫자
  

      Math.round(6.24) //6
      Math.floor(6.24) // 6
      Math.ceil(6.24) // 7
      Math.trunc(6.24) //6

 */
