/* _____________ 자바스크립트의 기초 _____________ */

/*
  자바스크립트는 웹 브라우저에서 동작하는 프로그래밍 언어로서
  HTML, 스타일시트와 함께 동적 웹 페이지의 중요한 구성 요소다.
  다음과 같은 다양한 기능을 가진다.

  - 문자,숫자,배열 등의 데이터를 다룬다.
  - 폼(form)을 조작한다.
  - 날짜와 시간을 다룬다.
  - 애니메이션 효과를 처리한다.
  - 브라우저를 조작한다.
  - 이미지/사운드/영상을 다룬다.
  - 이벤트를 처리한다.
  - 데이터를 송수신한다.
  - 페이지의 데이터 요소를 조작한다.
  - 로컬 데이터를 다룬다.
*/